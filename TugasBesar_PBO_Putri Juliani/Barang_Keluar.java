    private void autonumber(){
        try {
            Connection c = koneksi.getKoneksi();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM barang_keluar ORDER BY ID_Barang DESC";
            ResultSet r = s.executeQuery(sql);
            if (r.next()) {
                String NoFaktur = r.getString("ID_Barang").substring(2);
                String BKR = "" +(Integer.parseInt(NoFaktur)+1);
                String Nol = "";
                
                if (BKR.length()==1) 
                    {Nol = "00";}
                else if(BKR.length()==2)
                    {Nol = "0";}
                else if(BKR.length()==3)
                    {Nol = "";}
                
                txIDBarang.setText("BKR" + Nol + BKR);  
            }else{
                txIDBarang.setText("BKR001");
            }
            r.close();
            s.close();
        } catch (Exception e) {
            System.out.println("autonumber error");
        }
    }
    
