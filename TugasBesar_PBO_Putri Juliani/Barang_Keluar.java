    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        txtSimpan.setEnabled(false);
        txtEdit.setEnabled(true);
        txtHapus.setEnabled(true);
        txtBatal.setEnabled(true);
        
        int i = jTable1.getSelectedRow();
        if (i == -1) {
            return;
        }
        
        String id = (String) model.getValueAt(i, 0);
        String tanggal = (String) model.getValueAt(i, 1);
        String Customer = (String) model.getValueAt(i, 2);
        String namabarang = (String) model.getValueAt(i, 3);
        String harga = (String) model.getValueAt(i, 4);
        String stok = (String) model.getValueAt(i, 5);
        
        txIDBarang.setText(id);
        txtTanggal.setText(tanggal);
        txtCustomer.setSelectedItem(Customer);
        txtNamaBarang.setText(namabarang);
        txtHarga.setText(harga);
        txtStok.setText(stok);
    }//GEN-LAST:event_jTable1MouseClicked

