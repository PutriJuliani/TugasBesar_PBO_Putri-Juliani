    public void loadData(){
        model.getDataVector().removeAllElements();
        
        model.fireTableDataChanged();
        
        try {
            Connection c = koneksi.getKoneksi();
            Statement s = c.createStatement();
            
            String sql = "SELECT * FROM barang_keluar";
            ResultSet r = s.executeQuery(sql);
            
            while (r.next()) {
                Object[] BKR = new Object[6];
                BKR [0] = r.getString("ID_Barang");
                BKR [1] = r.getString("Tanggal_keluar");
                BKR [2] = r.getString("Customer");
                BKR [3] = r.getString("NamaBarang");
                BKR [4] = r.getString("harga");
                BKR [5] = r.getString("stok");
                
                model.addRow(BKR);
            }
            r.close();
            s.close();
        } catch (Exception e) {
            System.out.println("terjadi kesalahan");
        }
    }
    
